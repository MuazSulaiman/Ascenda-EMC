CREATE OR REPLACE VIEW v_target_year_summary AS
SELECT
    ty.year,
    ty.target_amount,
    ty.target_visits,
    CASE WHEN ty.target_visits > 0
         THEN ROUND(ty.target_amount / ty.target_visits, 2)
         ELSE NULL END                                    AS productivity,
    ty.status,
    COUNT(DISTINCT tr.id)                                 AS rep_count,
    COALESCE(SUM(tr.target_amount), 0)                    AS allocated_amount,
    COALESCE(SUM(tr.target_visits), 0)                    AS allocated_visits,
    ty.target_amount - COALESCE(SUM(tr.target_amount), 0) AS remaining_amount,
    ty.target_visits - COALESCE(SUM(tr.target_visits), 0) AS remaining_visits,
    ty.created_at,
    ty.updated_at
FROM target_year ty
LEFT JOIN target_rep tr ON tr.year = ty.year
GROUP BY ty.year, ty.target_amount, ty.target_visits,
         ty.status, ty.created_at, ty.updated_at;

CREATE OR REPLACE VIEW v_target_rep_summary AS
SELECT
    tr.id                                                  AS target_rep_id,
    tr.year,
    tr.user_id,
    u.name                                                 AS rep_name,
    u.region                                               AS rep_region,
    u.role                                                 AS rep_role,
    tr.target_amount,
    tr.target_visits,
    CASE WHEN tr.target_visits > 0
         THEN ROUND(tr.target_amount / tr.target_visits, 2)
         ELSE NULL END                                     AS productivity,
    COALESCE(SUM(tb.target_amount), 0)                     AS breakdown_amount,
    COALESCE(SUM(tb.target_visits), 0)                     AS breakdown_visits,
    tr.target_amount - COALESCE(SUM(tb.target_amount), 0)  AS unallocated_amount,
    tr.target_visits - COALESCE(SUM(tb.target_visits), 0)  AS unallocated_visits,
    COUNT(tb.id)                                           AS breakdown_row_count
FROM target_rep tr
JOIN users u ON u.user_id = tr.user_id
LEFT JOIN target_breakdown tb ON tb.target_rep_id = tr.id
GROUP BY tr.id, tr.year, tr.user_id, u.name,
         u.region, u.role, tr.target_amount, tr.target_visits;

