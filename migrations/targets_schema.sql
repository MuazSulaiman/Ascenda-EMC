-- ── product_categories ───────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS product_categories (
    product_category_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    business_unit_id    INTEGER NOT NULL REFERENCES business_units(business_unit_id),
    name                TEXT    NOT NULL,
    is_active           BOOLEAN NOT NULL DEFAULT TRUE,
    UNIQUE (business_unit_id, name)
);

-- ── business_lines: add product_category_id FK (nullable, non-breaking) ──────
ALTER TABLE business_lines
    ADD COLUMN IF NOT EXISTS product_category_id INTEGER
    REFERENCES product_categories(product_category_id);

-- ── target_year ──────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS target_year (
    year           INTEGER       PRIMARY KEY,
    target_amount  NUMERIC(18,2) NOT NULL DEFAULT 0 CHECK (target_amount >= 0),
    target_visits  INTEGER       NOT NULL DEFAULT 0 CHECK (target_visits >= 0),
    status         TEXT          NOT NULL DEFAULT 'DRAFT'
                   CHECK (status IN ('DRAFT', 'ACTIVE', 'LOCKED')),
    created_at     TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
    created_by     INTEGER       REFERENCES users(user_id),
    updated_at     TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
    updated_by     INTEGER       REFERENCES users(user_id)
);

-- ── target_rep ───────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS target_rep (
    id             BIGINT        GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    year           INTEGER       NOT NULL REFERENCES target_year(year) ON DELETE CASCADE,
    user_id        INTEGER       NOT NULL REFERENCES users(user_id),
    target_amount  NUMERIC(18,2) NOT NULL DEFAULT 0 CHECK (target_amount >= 0),
    target_visits  INTEGER       NOT NULL DEFAULT 0 CHECK (target_visits >= 0),
    created_at     TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
    created_by     INTEGER       REFERENCES users(user_id),
    updated_at     TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
    updated_by     INTEGER       REFERENCES users(user_id),
    UNIQUE (year, user_id)
);

-- ── target_breakdown ─────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS target_breakdown (
    id                  BIGINT        GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    target_rep_id       BIGINT        NOT NULL REFERENCES target_rep(id) ON DELETE CASCADE,
    year                INTEGER       NOT NULL,
    user_id             INTEGER       NOT NULL,
    breakdown_level     TEXT          NOT NULL
                        CHECK (breakdown_level IN (
                            'rep','customer','business_unit',
                            'product_category','business_line','article'
                        )),
    customer_id         INTEGER       REFERENCES customers(customer_id),
    business_unit_id    INTEGER       REFERENCES business_units(business_unit_id),
    product_category_id INTEGER       REFERENCES product_categories(product_category_id),
    business_line_id    INTEGER       REFERENCES business_lines(business_line_id),
    article_id          TEXT          REFERENCES items(product_id),
    target_amount       NUMERIC(18,2) NOT NULL DEFAULT 0 CHECK (target_amount >= 0),
    target_visits       INTEGER       NOT NULL DEFAULT 0 CHECK (target_visits >= 0),
    created_at          TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
    created_by          INTEGER       REFERENCES users(user_id),
    updated_at          TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
    updated_by          INTEGER       REFERENCES users(user_id)
);

